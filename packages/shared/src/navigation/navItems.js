function isWellnessNavItemEnabled(itemId) {
  if (itemId === 'fitness-assessment') return featureFlags.customer.fitnessAssessment === true;
  if (itemId === 'nail-assessment') return featureFlags.customer.nailHealthAssessment === true;
  return true;
}

export function getNavItemsForRole(role) {
  if (!role) return [];
  const actualUserRole = role || 'customer';
  let items = navItemsByRole[actualUserRole] || navItemsByRole.customer;

  items = items.filter((item) => isWellnessNavItemEnabled(item.id));

  if (actualUserRole !== 'super_admin') {
    items = items.filter((item) => {
      if (item.id === 'schedule') {
        if (['technician', 'cashier'].includes(actualUserRole)) {
          return featureFlags.staff.scheduleView === true;
        }
        return featureFlags.staff.staffShifts === true;
      }

      if (item.id === 'gift-cards') {
        if (actualUserRole === 'customer') {
          return featureFlags.customer.giftCards === true;
        }
        return featureFlags.staff.giftCards === true;
      }

      const mapping = navItemFeatureMappings[item.id];
      if (!mapping) return true;

      const flagsToCheck = Array.isArray(mapping) ? mapping : [mapping];
      return flagsToCheck.some((flag) => {
        const [featureArea, featureName] = flag.split('.');
        return featureFlags[featureArea]?.[featureName] === true;
      });
    });
  }

  return items.map((item) => {
    if (item.id === 'schedule' && ['technician', 'cashier'].includes(actualUserRole)) {
      return { ...item, href: getMySchedulePath(actualUserRole) };
    }
    if (item.id === 'schedule') {
      return { ...item, href: getStaffPlannerPath(actualUserRole) };
    }
    if (item.id === 'tips' && actualUserRole === 'technician') {
      return { ...item, href: getMyTipsPath(actualUserRole) };
    }
    if (item.id === 'fitness-assessment') {
      return { ...item, href: getFitnessAssessmentPath(actualUserRole) };
    }
    if (item.id === 'nail-assessment') {
      return { ...item, href: getNailAssessmentPath(actualUserRole) };
    }
    if (item.id === 'reviews') {
      return { ...item, href: getReviewsPath(actualUserRole) };
    }
    return item;
  });
}

